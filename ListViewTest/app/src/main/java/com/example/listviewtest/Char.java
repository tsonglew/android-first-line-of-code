package com.example.listviewtest;

public class Char {
    private String name;
    private int imageId;

    public Char(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return this.name;
    }

    public int getImageId() {
        return this.imageId;
    }
}
